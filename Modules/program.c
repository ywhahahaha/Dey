void _itoa(int i, char *string)
{
    int power = 0, j = 0;
    j = i;

    for(power = 1; j > 10; j /= 10)
    { power *= 10; }

    for(; power > 0; power /= 10) {
        *string++ = '0' + i / power;
        i %= power;
    }

    *string = '\0';
}


char *itoa(int num, char *str, int radix)
{
    char index[] = "0123456789ABCDEF";
    unsigned unum;/*中间变量*/
    int i = 0, j, k;

    /*确定unum的值*/
    if(radix == 10 && num < 0) { /*十进制负数*/
        unum = (unsigned) - num;
        str[i++] = '-';
    } else {
        unum = (unsigned)num;    /*其他情况*/
    }

    /*转换*/
    do {
        str[i++] = index[unum % (unsigned)radix];
        unum /= radix;
    } while(unum);

    str[i] = '\0';

    /*逆序*/
    if(str[0] == '-')
    { k = 1; } /*十进制负数*/
    else
    { k = 0; }

    char temp;

    for(j = k; j <= (i - 1) / 2; j++) {
        temp = str[j];
        str[j] = str[i - 1 + k - j];
        str[i - 1 + k - j] = temp;
    }

    return str;
}

/*************************************************
*@Function: bcd2str_float bcd码转字符串浮点
*@Input:    bcd ：bcd码数据指针
                    ret ：接受字符串指针
                    length：bcd码总字节数 (比如967295.01,length为4)
                    id      ：小数点所在的字节数位置 (比如967295.01,id为3)
*@Return: void
*@Others: 传输的字节低位在前 (967295.11 ,bcd[0]为0x11)
*************************************************/
void bcd2str_float(uint8_t *bcd, char *ret, uint32_t length, int id)
{
    uint32_t size = length;
    uint32_t j = 0;

    for(int i = 0; i < size; i++) {
        if(i == id)
        { ret[j++] = '.'; }

        ret[j++] = (bcd[size - 1 - i] >> 4) + '0';
        ret[j++] = (bcd[size - 1 - i] & 0x0f) + '0';
    }

    ret[j++] = 0;
}


/*************************************************
*@Function: bcd2str_int bcd码转字符串整形
*@Input:    bcd ：bcd码数据指针
                    ret ：接受字符串指针
                    length：bcd码总字节数 (比如967295,length为3)
*@Return: void
*@Others: 传输的字节高字节在前 (967295 ,bcd[0]为0x95)
*************************************************/
void bcd2str_int(uint8_t *bcd, char *ret, uint32_t length)
{
    uint32_t size = length;
    uint32_t j = 0;

    for(int i = 0; i < size; i++) {
        ret[j++] = (bcd[size - 1 - i] >> 4) + '0';
        ret[j++] = (bcd[size - 1 - i] & 0x0f) + '0';
    }

    ret[j++] = 0;
}

uint8_t crc_check(uint8_t *ptr, uint32_t len)
{
    uint32_t crc = 0, crc_get;
    crc = CRC16_lookup(ptr, len - 2);
    crc_get = (ptr[len - 2] & 0xff) + (ptr[len - 1] << 8);
    return (crc == crc_get);
}


/*************************************************
*@Function: 校验和校验
*@Input:    ptr ：需校验数据指针
                    len ：校验长度
*@Return: 校验和
*@Others:
*************************************************/
uint32_t sum_check(const uint8_t *ptr, uint32_t len)
{
    uint32_t sum = 0, i;

    for(i = 0; i < len; i++) {
        sum += *ptr;
        ptr++;
    }

    return sum;
}

