// 函数功能: CRC16效验,查表法
// 输入参数: pDataIn: 数据地址
//           iLenIn: 数据长度
// 输出参数: pCRCOut: 2字节校验值
WORD CRC16_lookup(BYTE *pucFrame, int usLen)
{
    WORD iIndex;
    WORD ucCRCHi = 0xFF;
    WORD ucCRCLo = 0xFF;

    while(usLen--) {
        iIndex = ucCRCLo^*(pucFrame++);
        ucCRCLo = (BYTE)(ucCRCHi ^ aucCRCHi[iIndex]);
        ucCRCHi = aucCRCLo[iIndex];
    }

    return(WORD)(ucCRCHi << 8 | ucCRCLo);
}



