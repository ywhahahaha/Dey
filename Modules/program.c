char *my_iota(int var, int radix)
{
    static char local[33] = {0};
    char *p = &local[32];
    static unsigned char table[] = "0123456789abcdef";
    bool sign = false;
    unsigned int tmp;

    if(radix < 2 && radix > 16) {
        *p = '\0';
        return p;
    }

    if(var < 0 && radix == 10) {
        sign = true;
        var = -var;
    }

    tmp = var;
    *p-- = '\0';

    do {
        *p-- = table[tmp % radix];
        tmp /= radix;
    } while(tmp > 0);

    if(sign == true) {
        *p-- = '-';
    }

    return p + 1;
}


/****************************************************************************
 str_to_hex
*****************************************************************************/
int str_to_hex(char *string, unsigned char *cbuf, int len)
{
    BYTE high, low;
    int idx, ii = 0;

    for(idx = 0; idx < len; idx += 2) {
        high = string[idx];
        low = string[idx + 1];

        if(high >= '0' && high <= '9')
        { high = high - '0'; }
        else if(high >= 'A' && high <= 'F')
        { high = high - 'A' + 10; }
        else if(high >= 'a' && high <= 'f')
        { high = high - 'a' + 10; }
        else
        { return -1; }

        if(low >= '0' && low <= '9')
        { low = low - '0'; }
        else if(low >= 'A' && low <= 'F')
        { low = low - 'A' + 10; }
        else if(low >= 'a' && low <= 'f')
        { low = low - 'a' + 10; }
        else
        { return -1; }

        cbuf[ii++] = high << 4 | low;
    }

    return 0;
}

