  /*  增益 */
  /* MATLAB Function 'SOC_EKF/协方差矩阵更新': '<S5>:1' */
  /* '<S5>:1:2' C = [C(1) C(2)]; */
  /* '<S5>:1:3' P = [P_Cov(1) P_Cov(2) ; P_Cov(3) P_Cov(4)]; */
  /* '<S5>:1:4' P1 = P - K * C * P; */
  A1_0[0] = rtb_K_idx_0 * rtb_C1;
  A1_0[1] = rtb_K_idx_1 * rtb_C1;
  A1_0[2] = -rtb_K_idx_0;
  A1_0[3] = -rtb_K_idx_1;
