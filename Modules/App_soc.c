  rtb_C1 = SOC_EKF_ConstB.Fcn1*SAMPLE_INTERVAL/init_cap * SOC_EKF_DW[_bat_pack_label].Delay1_DSTATE +
    SOC_EKF_DW[_bat_pack_label].UnitDelay_DSTATE;

