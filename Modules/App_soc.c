float Cap_convert(int real_cur,int real_temp,unsigned char bat_state,unsigned char bat_pack_label)
{
  float cur_coeff = 0;
  float cap_coeff = 1;
  switch (bat_state)
  {
    case 2://放电
      cur_coeff =abs(real_cur)*0.1/_nominal_cap[bat_pack_label-1];
      cap_coeff = Cap_initial(real_temp*1.0/10, cur_coeff,  
        Cap_init_ConstP.temp_Data,
        Cap_init_ConstP.curcoeff_Data,
        Cap_init_ConstP.cap_tableData,
        Cap_init_ConstP.maxIndex, 8U);
     _initial_cap[bat_pack_label-1] = cap_coeff*_nominal_cap[bat_pack_label-1]; 
     break;

   default://充电,浮充，静置
     _initial_cap[bat_pack_label-1] = _nominal_cap[bat_pack_label-1];
     break;
  }
  return cap_coeff;
}

unsigned char App_deep_cycle_time(float *deep_cycle_time,int real_cur,int real_temp,unsigned char bat_state,unsigned char bat_pack_label)
{
  float deep_cycle =0,remain_deep_cycle;

  float temp_coeff =1,cur_coeff =1,cur_cap_coeff=1;
  cur_cap_coeff = abs(real_cur)*0.1/_nominal_cap[bat_pack_label-1];
  if(cur_cap_coeff>0.1f && cur_cap_coeff<=1)
    cur_coeff = -0.05f*cur_cap_coeff*10+1.05f;
  else
    cur_coeff =1;
  if(real_temp <0)
    temp_coeff =0.8;
  else if(real_temp>=0&&real_temp<100)
    temp_coeff =0.9;
  else if(real_temp >=400)
    temp_coeff = 1.1;
  else
    temp_coeff =1.0; 
  switch (bat_state)
  {
    case 1://充电
      _charge_add_cap[bat_pack_label-1] = _charge_add_cap[bat_pack_label-1]+real_cur*0.1/3600;
      break;
    
    case 2://放电
      _discharge_add_cap[bat_pack_label-1] = _discharge_add_cap[bat_pack_label-1]+abs(real_cur)*0.1/3600;
      break;
  }
  if(_charge_add_cap[bat_pack_label-1]!=0||_discharge_add_cap[bat_pack_label-1]!=0)
  {
    deep_cycle = (_charge_add_cap[bat_pack_label-1]+_discharge_add_cap[bat_pack_label-1])/(2*_nominal_cap[bat_pack_label-1]*temp_coeff*cur_coeff);
    if(deep_cycle>=0.001*_deep_cycle_time[bat_pack_label-1])
    {
       *deep_cycle_time = *deep_cycle_time-deep_cycle; 
      _charge_add_cap[bat_pack_label-1] =0;
      _discharge_add_cap[bat_pack_label-1] =0;
      if(*deep_cycle_time<=0)
      {
       *deep_cycle_time =0;
      }
      _diff_deep_cycle[bat_pack_label-1] = _diff_deep_cycle[bat_pack_label-1]+deep_cycle;
      #ifdef SOC_PRINTF 
       vs_printf("deep_cycle_time_%d=%.3f\r\n",bat_pack_label,*deep_cycle_time);  
      #endif 
       return 1;
    } 
  }
  return 0;
}
 /**
 * @name: 
 * @msg: 
 * @param {float} initial_cap:标称容量
 * @param {float} initial_soc：初始容量百分比
 * @param {float} float_charge_vol：浮充电压
 * @param {float} max_cycle_use_vol：均充压最大值
 * @param {float} mix_cycle_use_vol：放电截至电压
 * @return NULL
  */
void App_soc_intial(float initial_cap,unsigned int initial_soc,float real_vol, float float_charge_vol,float max_cycle_use_vol,float mix_cycle_use_vol,unsigned char bat_state,unsigned char bat_pack_label,unsigned char soc_retain_flag)
{
  float init_soc = 1;
  _max_vol[bat_pack_label-1] = max_cycle_use_vol;
  _min_vol[bat_pack_label-1] = mix_cycle_use_vol; 
  _float_vol[bat_pack_label-1] =  float_charge_vol; 
  _nominal_cap[bat_pack_label-1] = initial_cap;
  if (soc_retain_flag ==1)
  {
    init_soc = (float)initial_soc/1000.0f; 
  }
  else
  {
    switch (bat_state)
    {
    case 1://充电
      init_soc =(real_vol-mix_cycle_use_vol)/(max_cycle_use_vol-mix_cycle_use_vol);
      break;

    case 2://放电
      init_soc =(real_vol-mix_cycle_use_vol)/(max_cycle_use_vol*DISCHARG_COEF-mix_cycle_use_vol);
      break;

    case 0://静置
      init_soc =(real_vol-mix_cycle_use_vol)/(max_cycle_use_vol*DISCHARG_COEF-mix_cycle_use_vol);
      break;

    default://浮充
      init_soc = 1 ;
      break;
    }
  }
  if(init_soc >= 1)
    init_soc = 1;
  else if(init_soc <= 0)
    init_soc = 0;  
  SOC_EKF_Y[bat_pack_label-1].SOC_upd = init_soc;  
  return_soc[bat_pack_label-1] = (unsigned int)(init_soc*AMP_MUL);
  Model_simulation_Y[bat_pack_label-1].SOC_real = init_soc;
  Model_simulation_initialize(init_soc,bat_pack_label-1); 
  Noise_initialize();
  //SOC_EKF_initialize(init_soc,max_cycle_use_vol*DISCHARG_COEF,bat_pack_label-1);
  SOC_EKF_initialize(init_soc,real_vol,bat_pack_label-1);
  #ifdef SOC_PRINTF 
  vs_printf("init_soc_%d=%.3f\r\n",bat_pack_label-1,init_soc);  
  #endif  
}
/**
 * @name: 
 * @msg: Model_simulation_step：输入采集电流，输出预估电压和预估SOC；Noise_step：输入采集电流，输出噪声电流；SOC_EKF_step：输入噪声电流，输出EKF算法SOC
 * @msg: 初始上电1分钟自校准
 * @param {float} real_vol:实时电压
 * @param {float} real_cur:实时电流，放电时为-，充电时为+
 * @param {float} temp:实时温度
 * @param {float} clk_in_msec：计算时间
 * @param {unsigned char} bat_state：电池状态
 * @return {float}SOC,单位%
 */
unsigned int App_soc_process(float real_vol,float real_cur,float temp,float clk_in_sec,unsigned char bat_state,unsigned char bat_pack_label)
{
  unsigned int step_num =0;
  unsigned int step_time_num = clk_in_sec/SAMPLE_INTERVAL;
  // static float return_soc = 1;
  int  return_soc_buff =0;
  unsigned char i =0;
  unsigned char _bat_state =0;
  static unsigned char num[BAT_PACK_NUM] ={0};
  static unsigned char pre_bat_state[BAT_PACK_NUM] ={0};
  static unsigned char next_bat_state[BAT_PACK_NUM] ={0}; 
  _bat_state = bat_state;
  pre_bat_state[bat_pack_label-1] = bat_state; 

  if(_bat_state ==0||_bat_state==3)
  {
    if(real_vol >= (_float_vol[bat_pack_label-1]-FLOAT_VOLT_OFFSET))
      _bat_state = 3;
  }
  switch (_bat_state)
  {
  case 1://充电
    rint_coef[bat_pack_label-1]=1.0E-5;
    real_cur=fabs(real_cur);
    for(i=0;i<10;i++)
    {
      OCV_tableDate[bat_pack_label-1][i]=(_max_vol[bat_pack_label-1] - _min_vol[bat_pack_label-1])*0.1f*(i+1)+_min_vol[bat_pack_label-1];
    }    
    // return_soc[bat_pack_label-1] = (int)(SOC_EKF_Y[bat_pack_label-1].SOC_upd*AMP_MUL);
    // return_soc[bat_pack_label-1] = (int)(Model_simulation_Y[bat_pack_label-1].SOC_real*AMP_MUL ) ;
    if(return_soc[bat_pack_label-1] >= 1000)
    {
      Model_simulation_DW.Memory1_PreviousInput[bat_pack_label-1] = 1;
      Model_simulation_DW.Memory_PreviousInput[0][bat_pack_label-1] = 1;
      SOC_EKF_DW[bat_pack_label-1].UnitDelay_DSTATE = 1;
      SOC_EKF_Y[bat_pack_label-1].SOC_upd =1;
      return_soc[bat_pack_label-1] = 1000;
      return return_soc[bat_pack_label-1];
    }
    break;
  case 2://放电
    rint_coef[bat_pack_label-1]=1.0/6000;
    real_cur=-1*fabs(real_cur);
    for(i=0;i<10;i++)
    {
      OCV_tableDate[bat_pack_label-1][i]=(_max_vol[bat_pack_label-1]*DISCHARG_COEF - _min_vol[bat_pack_label-1])*0.1f*(i+1)+_min_vol[bat_pack_label-1];
    }  
    // return_soc[bat_pack_label-1] = (int)(SOC_EKF_Y[bat_pack_label-1].SOC_upd*AMP_MUL); 
    // return_soc[bat_pack_label-1] = (int)(Model_simulation_Y[bat_pack_label-1].SOC_real*AMP_MUL ) ;
    if(return_soc[bat_pack_label-1] <= 0)
    {
      Model_simulation_DW.Memory1_PreviousInput[bat_pack_label-1] = 0;
      Model_simulation_DW.Memory_PreviousInput[0][bat_pack_label-1] = 0;
      SOC_EKF_DW[bat_pack_label-1].UnitDelay_DSTATE = 0;
      SOC_EKF_Y[bat_pack_label-1].SOC_upd =0;
      return_soc[bat_pack_label-1] = 0;      
      return return_soc[bat_pack_label-1];
    }
    break; 
  case 0://静置
    {
      rint_coef[bat_pack_label-1]=1.0f/6000;
      for(i=0;i<10;i++)
      {   
        OCV_tableDate[bat_pack_label-1][i]=(_max_vol[bat_pack_label-1]*DISCHARG_COEF- _min_vol[bat_pack_label-1])*0.1f*(i+1)+_min_vol[bat_pack_label-1];
      } 
      if(return_soc[bat_pack_label-1] < 0)
      {
        Model_simulation_DW.Memory1_PreviousInput[bat_pack_label-1] = 0;
        Model_simulation_DW.Memory_PreviousInput[0][bat_pack_label-1] = 0;
        SOC_EKF_DW[bat_pack_label-1].UnitDelay_DSTATE = 0;
        SOC_EKF_Y[bat_pack_label-1].SOC_upd =0;
      }
      else if(return_soc[bat_pack_label-1] > 1000)
      {
        Model_simulation_DW.Memory1_PreviousInput[bat_pack_label-1] = 1;
        Model_simulation_DW.Memory_PreviousInput[0][bat_pack_label-1] = 1;
        SOC_EKF_DW[bat_pack_label-1].UnitDelay_DSTATE = 1;
        SOC_EKF_Y[bat_pack_label-1].SOC_upd =1;     
      }
      else
      {
        Model_simulation_DW.Memory1_PreviousInput[bat_pack_label-1] = SOC_EKF_Y[bat_pack_label-1].SOC_upd;
        Model_simulation_DW.Memory_PreviousInput[0][bat_pack_label-1] = SOC_EKF_Y[bat_pack_label-1].SOC_upd ;
        SOC_EKF_DW[bat_pack_label-1].UnitDelay_DSTATE = SOC_EKF_Y[bat_pack_label-1].SOC_upd ; 
        next_bat_state[bat_pack_label-1] = pre_bat_state[bat_pack_label-1];  
        return return_soc[bat_pack_label-1];
      }
    }
    break;      
  default://浮充
    {
      Model_simulation_DW.Memory1_PreviousInput[bat_pack_label-1] = 1;
      Model_simulation_DW.Memory_PreviousInput[0][bat_pack_label-1] = 1;
      SOC_EKF_DW[bat_pack_label-1].UnitDelay_DSTATE = 1;
      SOC_EKF_Y[bat_pack_label-1].SOC_upd =1;
      return_soc[bat_pack_label-1] =1000;
      next_bat_state[bat_pack_label-1] = pre_bat_state[bat_pack_label-1];  
      return return_soc[bat_pack_label-1];
    }
    break;
  } 
  if(next_bat_state[bat_pack_label-1] != pre_bat_state[bat_pack_label-1])
  {
    num[bat_pack_label-1]++;
    Model_simulation_DW.Memory1_PreviousInput[bat_pack_label-1] = SOC_EKF_Y[bat_pack_label-1].SOC_upd;
    Model_simulation_DW.Memory_PreviousInput[0][bat_pack_label-1] = SOC_EKF_Y[bat_pack_label-1].SOC_upd;  
    SOC_EKF_DW[bat_pack_label-1].UnitDelay_DSTATE = SOC_EKF_Y[bat_pack_label-1].SOC_upd; 
  }
   if(num[bat_pack_label-1]>=3)//电池状态不相等次数
  {
    num[bat_pack_label-1] = 0;
    next_bat_state[bat_pack_label-1] = pre_bat_state[bat_pack_label-1];
  }
  //算法
 for ( step_num = 0; step_num < step_time_num; step_num++)
 {
    Model_simulation_U[bat_pack_label-1].I_real=-1*real_cur;
    Model_simulation_step(_nominal_cap[bat_pack_label-1],bat_pack_label-1);
    Noise_U.Variance=NOISE_VARIANCE;
    Noise_U.In1=-1*real_cur;    
    Noise_step(); 
    SOC_EKF_U.u_f=Noise_Y.Out1;
    SOC_EKF_U.u=Model_simulation_Y[bat_pack_label-1].UL_obs;
    // SOC_EKF_U.u = real_vol;
    SOC_EKF_step(_nominal_cap[bat_pack_label-1],bat_pack_label-1);
  }
   return_soc_buff = (int)(SOC_EKF_Y[bat_pack_label-1].SOC_upd*AMP_MUL);
  // return_soc_buff = (int)(Model_simulation_Y[bat_pack_label-1].SOC_real*AMP_MUL ) ;

  if(bat_state == 1)
    {
      if(return_soc[bat_pack_label-1] <= return_soc_buff)
        return_soc[bat_pack_label-1] = return_soc_buff; 
    }
  else if(bat_state == 2)
    {
      if(return_soc[bat_pack_label-1] >= return_soc_buff)
        return_soc[bat_pack_label-1] = return_soc_buff; 
    }
  else
    return_soc[bat_pack_label-1] = return_soc_buff;  

  #ifdef SOC_PRINTF 
 
  vs_printf("Model_simulation.vol=%f,real_vol=%f,bat_pack=%d\r\n",Model_simulation_Y[bat_pack_label-1].UL_obs,real_vol,bat_pack_label);  
  vs_printf("Model_simulation.soc=%f,ekf_soc=%f,bat_pack=%d\r\n",Model_simulation_Y[bat_pack_label-1].SOC_real,SOC_EKF_Y[bat_pack_label-1].SOC_upd,bat_pack_label);
  vs_printf("return_soc_%d=%d\r\n",bat_pack_label,return_soc[bat_pack_label-1]);
  #endif
  if(return_soc[bat_pack_label-1]< 0)
  {
    Model_simulation_DW.Memory1_PreviousInput[bat_pack_label-1] = 0;
    Model_simulation_DW.Memory_PreviousInput[0][bat_pack_label-1] = 0;
    SOC_EKF_DW[bat_pack_label-1].UnitDelay_DSTATE = 0;
    return_soc[bat_pack_label-1] = 0;
  }
  else if(return_soc[bat_pack_label-1] > 1000)
  {
    Model_simulation_DW.Memory1_PreviousInput[bat_pack_label-1] = 1;
    Model_simulation_DW.Memory_PreviousInput[0][bat_pack_label-1] = 1;
    SOC_EKF_DW[bat_pack_label-1].UnitDelay_DSTATE = 1;
    return_soc[bat_pack_label-1] = 1000;
  }
  return return_soc[bat_pack_label-1];
} 
/**
 * @name: 
 * @msg: 在放电状态下备电时长
 * @param {unsigned char} bat_pack_label：电池组标签
 * @param {unsigned char} bat_state：电池状态
 * @return (float)电池备电时长
 */
unsigned int App_standby_time(unsigned char bat_pack_label,float real_cur,unsigned char bat_state)
{
  float standby_time =0;    
  if(bat_state==0 || bat_state==2 ||real_cur<=0)
  {
    if(real_cur ==0&&return_soc[bat_pack_label-1]>=1)
      return 65535;
    else if(real_cur ==0&&return_soc[bat_pack_label-1]==0)
      return 0;
    else
    {
      if(real_cur<0)
      {
        standby_time = _nominal_cap[bat_pack_label-1]*return_soc[bat_pack_label-1]/100/fabs(real_cur); 
        if(standby_time >=65535) 
          standby_time =65535; 
        return  (unsigned int)standby_time;
      }
      else
        return 65535;  
    }
  }
  else
    return 65535;
}
