  A1[2] = 0.0;
  A1[1] = 0.0;

  /* Outputs for Atomic SubSystem: '<S1>/A矩阵' */
  A1[3] = rtb_Fcn_o_tmp;

  /* End of Outputs for SubSystem: '<S1>/A矩阵' */
  /* '<S6>:1:3' Qs = StaNoise(1); */
  /* '<S6>:1:4' Qu = StaNoise(2); */
