    P2[i] += A1[i] * rtb_K_idx_0;
    rtb_C1 = A1[i + 2];
    P2[i] += rtb_C1 * tmp;
    P2[i + 2] = 0.0;
    P2[i + 2] += A1[i] * rtb_K_idx_1;
    P2[i + 2] += rtb_C1 * tmp_0;
    A1_0[i] = 0.0;
    A1_0[i] += P2[i];
    A1_0[i] += P2[i + 2] * 0.0;
    A1_0[i + 2] = 0.0;
    A1_0[i + 2] += P2[i] * 0.0;

