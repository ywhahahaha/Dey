/**
 * @name: 
 * @msg:  每月调用1次
 * @param {unsigned char} bat_pack_label：电池组标签
 * @param {float} pre_soh：计算前soh
 * @return (float)电池SOH衰减，计算后soh
 */
unsigned int App_soh_process(unsigned char bat_pack_label)
{
  unsigned int diff_soh =0,integ_time=0;
  float deci_time=0;
  if(_deep_cycle_time[bat_pack_label-1]!=0)
  {
    integ_time= _diff_deep_cycle[bat_pack_label-1] /(0.001*_deep_cycle_time[bat_pack_label-1]);
    if(integ_time>=1)
    {
      diff_soh= (unsigned int)(_diff_deep_cycle[bat_pack_label-1]*1000)/_deep_cycle_time[bat_pack_label-1];
      deci_time =_diff_deep_cycle[bat_pack_label-1] /(0.001*_deep_cycle_time[bat_pack_label-1])-integ_time;
      _diff_deep_cycle[bat_pack_label-1] = deci_time;
    }
  } 
  _initial_soh[bat_pack_label-1]=_initial_soh[bat_pack_label-1]-diff_soh;
  _initial_soh[bat_pack_label-1]=_initial_soh[bat_pack_label-1]-(unsigned int)((float)((100-BAT_BY_SOH)/(_bat_life[bat_pack_label-1]*12))*10);
  if(_initial_soh[bat_pack_label-1]<=0)
  {
    _initial_soh[bat_pack_label-1] = 0;
  }
  return _initial_soh[bat_pack_label-1];
}
