  /* MATLAB Function 'SOC_EKF/卡尔曼增益计算': '<S7>:1' */
  /* '<S7>:1:2' C1 = [C(1) C(2)]; */
  /* '<S7>:1:3' P = [P_Cov(1) P_Cov(2) ;  P_Cov(3) P_Cov(4)]; */
  P2[0] = A1_0[0] + 0.001;
  P2[2] = A1_0[2];
  P2[1] = A1_0[1];
  P2[3] = A1_0[3] + 0.001;

  /* '<S7>:1:4' K = P*C1'* (C1*P*C1'+R)^(-1); */
