    __set_PRIMASK(0);

    HAL_Init();

    /* Configure the system clock to 168 MHz */
    SystemClock_Config();
    
    SystemCoreClockUpdate();
    
    HAL_SuspendTick();

    /* Initialize CMSIS-RTOS2 */
    osKernelInitialize();
    
